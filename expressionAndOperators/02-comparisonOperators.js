//? Equal
'3' == 3;
console.log('3' == 3); 
console.log('bob' == 'Bob')

// == is equal

/*
    - JavaScript is helpful and does something known as coercion when comparing values.
        - coercion is the process of converting a value from one type to another. 
*/

//? Strict Equal
console.log("3" === 3);
console.log(3 === 3);

// === is strictly equal

console.log('Bob' === 'Bobby'); 

//? Not Equal 
console.log('3' != 3)//false 

//? Strict Not Equal
console.log('3' !== 3);//true

//? Greater Than
3 > 2;

//? Less Than
2 < 3;

//? Greater Than or Equal To
3 >= 2; // fat arrow *not the same thing

//? Less Than or Equal To
2 <= 2;

//? And
console.log('two' && 'three');

//? Or
console.log(2 || 3);
